#include "3-calc.h"

/**
 * op_add - calculates the sum of two integers
 *
 * @a: First integer
 * @b: Second integer
 *
 * Return: the sum of the integers
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - calculates the difference of two integers
 *
 * @a: First integer
 * @b: Second integer
 *
 * Return: the difference of the integers
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - calculates the product of two integers
 *
 * @a: First integer
 * @b: Second integer
 *
 * Return: the product of the integers
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - calculates the division of two integers
 *
 * @a: First integer
 * @b: Second integer
 *
 * Return: the results of the division of the integers
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - calculates the modulus of two integers
 *
 * @a: First integer
 * @b: Second integer
 *
 * Return: the modulus of the integers
 */

int op_mod(int a, int b)
{
	return (a % b);
}
